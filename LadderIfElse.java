public class LadderIfElse {

    public static void main(String[] args) {
        int day = 4;
        if (day == 0) {
            System.out.println("Sun");
        } else if (day == 1) {
            System.out.println("Mon");

        } else if (day == 2) {
            System.out.println("Tue");

        } else if (day == 3) {
            System.out.println("Wed");

        }
        else{
            System.out.println("Other days");
        }
    }

}


